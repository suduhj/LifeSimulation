export function npcTemplateLabel(id) {
  return NPC_TEMPLATE_LABELS[id] ?? "";
}

export function identityLabel(id) {
  return IDENTITY_LABELS[id] ?? "普通家庭";
}

export function talentLabel(id, fallback = "") {
  const label = TALENT_LABELS[id] ?? fallback ?? "";
  return isPlayerSafeLabel(label) ? label : "";
}

export function npcRoleLabel(id) {
  return NPC_ROLE_LABELS[id] ?? "";
}

export function buildPlayerVisibleNpcIdentity({ templateId, role, roleTags = [] } = {}) {
  const templateLabel = npcTemplateLabel(templateId);
  const roleLabel = npcRoleLabel(role);
  const canKnowRole = isOrdinaryVisibleRole(role, roleTags);
  return {
    label: canKnowRole && roleLabel ? roleLabel : templateLabel,
    publicRole: templateLabel,
    roleHint: canKnowRole && roleLabel ? roleLabel : templateLabel,
    certainty: canKnowRole ? "known_surface" : "surface_only",
    discovered: false,
  };
}

export function visibleNpcLabel(npc) {
  const visible = npc?.playerVisible;
  if (visible?.label && !looksLikeInternalId(visible.label)) return visible.label;
  if (visible?.publicRole && !looksLikeInternalId(visible.publicRole)) return visible.publicRole;
  return npcTemplateLabel(npc?.templateId);
}

export function visibleTalentName(talent) {
  const candidates = [
    talent?.playerVisibleName,
    talent?.name,
    talentLabel(talent?.id),
  ];
  return candidates.find(isPlayerSafeLabel) ?? "";
}

export function isNpcHiddenFromOpening(npc) {
  const tags = new Set(npc?.roleTags ?? []);
  const hiddenRole = String(npc?.role ?? "");
  return (
    tags.has("hidden_senior") ||
    tags.has("official") ||
    tags.has("cult") ||
    tags.has("secret") ||
    tags.has("hidden_enemy") ||
    tags.has("demonic_path") ||
    tags.has("remnant_soul") ||
    tags.has("possession_risk") ||
    tags.has("corporation") ||
    tags.has("research") ||
    tags.has("raider") ||
    tags.has("large_faction") ||
    /hidden|secret|spy|possessor|experimenter|cult|official|enemy|threat|deceiver/.test(hiddenRole)
  );
}

export function sanitizePlayerText(text, npcs = []) {
  let result = String(text ?? "");
  result = result.replace(/[（(][^（）()]{0,30}(?:后来知道|后面知道|未来知道|日后知道|真正身份|真实身份)[^（）()]{0,80}[）)]/g, "");
  for (const npc of npcs) {
    const visible = visibleNpcLabel(npc);
    const rawValues = [
      npc.id,
      npc.templateId,
      npc.role,
      npc.knownIdentity?.role,
      npc.knownIdentity?.templateId,
    ].filter(Boolean);
    for (const raw of rawValues) {
      result = result.split(String(raw)).join(visible);
    }
  }
  result = result.replace(/\bNPC[_-]?\d+(?:\s*[\u4e00-\u9fff]{1,10})?/gi, "那个人");
  result = result.replace(/\bnpc[_-]?\d+(?:_[a-z0-9_]+)?\b/gi, "那个人");
  result = replaceKnownInternalTokens(result);
  return result;
}

function replaceKnownInternalTokens(text) {
  let result = String(text ?? "");
  const entries = [
    ...Object.entries(NPC_TEMPLATE_LABELS),
    ...Object.entries(NPC_ROLE_LABELS),
  ].sort((left, right) => right[0].length - left[0].length);
  for (const [key, label] of entries) {
    const pattern = new RegExp(`\\b${escapeTokenRegExp(key)}\\b`, "gi");
    result = result.replace(pattern, label);
  }
  return result;
}

function isPlayerSafeLabel(value) {
  const text = String(value ?? "").trim();
  if (!text) return false;
  if (looksLikeInternalId(text)) return false;
  if (/未命名天赋|未知天赋|unnamed|unknown/i.test(text)) return false;
  return true;
}

function escapeTokenRegExp(value) {
  return String(value ?? "").replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function isOrdinaryVisibleRole(role, roleTags) {
  const roleText = String(role ?? "");
  const tags = new Set(roleTags ?? []);
  if (tags.has("family") || tags.has("ordinary_life") || tags.has("friend") || tags.has("school")) return true;
  return /parent|friend|teacher|student|child|lover|spouse|healer|mentor|merchant|doctor/.test(roleText);
}

function looksLikeInternalId(value) {
  return /^[a-z][a-z0-9_-]*$/i.test(String(value ?? "").trim());
}
